# 데이터 타입 : 정수형 int
a = 123
print(a)
print(type(a))

# 데이터 타입 : 실수형 float
b = 1.2
print(b)
print(type(b))

# 덧셈(addition)
c = 3
d = 4
print(c + d)

# 곱셈(multiplication)
e = 3
f = 2
print(e * f)

# 제곱(square)
print(e ** f)

# 나눗셈(division)
g = 9
h = 3
print(g / h)

# 몫(quotient) 구하기
i = 10
j = 3
print(i // j)